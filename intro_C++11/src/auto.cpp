#include <iostream>

using namespace std;

int main() {

	auto myInt = 0;  // This is a int
	auto myDouble = 3.14;  // This is a double
	auto myString = "nothing";  // This is a string

	cout << "My int is " << myInt << endl;
	cout << "My double is " << myDouble << endl;
	cout << "My string is " << myString << endl;

	return 0;
}
